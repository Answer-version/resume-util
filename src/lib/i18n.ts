export type Dictionary = typeof dictionaries["zh-CN"];

export function getDictionary(locale: Locale) {
  return dictionaries[locale];
}

export function formatDateTimeForLocale(locale: Locale, value: string | Date) {
  return new Intl.DateTimeFormat(locale === "en" ? "en-US" : "zh-CN", {
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
  }).format(new Date(value));
}

export function translateGender(locale: Locale, value: string) {
  const dictionary = getDictionary(locale);

  if (value === "男") {
    return dictionary.male;
  }

  if (value === "女") {
    return dictionary.female;
  }

  if (value === "其他") {
    return dictionary.other;
  }

  return value;
}
